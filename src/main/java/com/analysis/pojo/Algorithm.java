package com.analysis.pojo;

public class Algorithm {

    private int algo_id;
    private String algo_name;
    private boolean algo_status;

    public int getAlgo_id() {
        return algo_id;
    }

    public void setAlgo_id(int algo_id) {
        this.algo_id = algo_id;
    }

    public String getAlgo_name() {
        return algo_name;
    }

    public void setAlgo_name(String algo_name) {
        this.algo_name = algo_name;
    }

    public boolean isAlgo_status() {
        return algo_status;
    }

    public void setAlgo_status(boolean algo_status) {
        this.algo_status = algo_status;
    }
}
